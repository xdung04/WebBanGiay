    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        String inputCode = request.getParameter("code");
        String sentCode = (String) session.getAttribute("verificationCode");
        Long codeCreationTime = (Long) session.getAttribute("codeCreationTime");

        // Check if the code has expired (1 minute = 60000 milliseconds)
        if (codeCreationTime != null && System.currentTimeMillis() - codeCreationTime > 60000) {
            session.setAttribute("messForgetPass", "Mã xác nhận đã hết hạn. Vui lòng thử lại.");
            session.removeAttribute("verificationCode");
            session.removeAttribute("codeCreationTime");
            response.sendRedirect("forgetPassword.jsp");
            return;
        }

        if (inputCode.equals(sentCode)) {
            // Code is correct, redirect to reset password page
            response.sendRedirect("resetPassword.jsp");
        } else {
            // Code is incorrect, show an error message
            request.setAttribute("error", "Mã xác nhận không đúng!");
            request.getRequestDispatcher("verify.jsp").forward(request, response);
        }
    }

